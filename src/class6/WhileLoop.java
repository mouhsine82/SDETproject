package class6;

public class WhileLoop {
    public static void main(String[] args) {

        int number2=0;
        while (number2<10){ // it will keep executing (looping) the code till the condition turn false which is less than 10.
            System.out.println("Hello world");
            number2++; // this line need to be inside the body { } otherwise will keep running the code.
        }









    }
}
