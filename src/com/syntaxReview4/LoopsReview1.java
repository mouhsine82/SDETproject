package com.syntaxReview4;

public class LoopsReview1 {
    public static void main(String[] args) {
        // Car odometer nested loop
        for (int i = 0; i <=9 ; i++) {
            for (int j = 0; j <=9; j++) {
                for (int k = 0; k <=9 ; k++) {
                    for (int l = 0; l <=9 ; l++) {
                        System.out.println(i+" "+j+" "+k+" "+l);
                    }

                }

            }

        }

    }
}
