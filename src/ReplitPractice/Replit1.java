package ReplitPractice;

import java.util.Scanner;

public class Replit1 {
    public static void main(String[] args) {

        Scanner input = new Scanner(System.in);

        int x = input.nextInt();
        for (int i = x - 1; i >= 0; i--) {

            System.out.print(i + " ");
        }


    }
}







