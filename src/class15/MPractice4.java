public class MPractice4 {

    /*
    create a method that takes 2 numbers as parameters and return the larger number
     */
// return type => int // name=> largerNum // parameters => int num1, num2.
    int largerNum(int num1, int num2) {
        if (num1 > num2) {
            return num1;
        } else {
            return num2;
        }

    }
}

